// 1. O dobro do número.
let dobroNumero = (n) => {
    return n * 2
}
console.log(dobroNumero(5))

// 2. O triplo do número.
let triploNumero = (n) => {
    return n * 3
}
console.log(triploNumero(5))

// 3. O quadrado do número.
let quadradoNumero = (n) => {
    return n ** 2
}
console.log(quadradoNumero(5))